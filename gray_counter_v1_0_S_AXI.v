module gray_Nbits (clk, clk_en, rst, control_reset, gray_out);
	parameter N = 8;
	parameter SIZE =(N+1);
	parameter Zeros = {SIZE{1'b0}};
	// Place the definition of wires and regs here
	input clk,rst,clk_en,control_reset;
	reg [N:0]state;
	reg [N:0]toggle;
	reg [N-1:0]check;
	output [N-1:0]gray_out;
	integer i,j,k;
	// The state of the Gray counter
	always @(posedge clk, posedge rst)
		begin
			if (rst == 1'b1)
				// Initialize state with 1000..00
				state = {1'b1,{N{1'b0}}};
			else begin
			if(control_reset)begin
				state = {1'b1,{N{1'b0}}};
            end	else
				begin
				if(clk_en)
					begin
					// Main part of code
					for(k = 0; k <= N; k = k+1)
						begin
							state[k] = state[k] + toggle[k];
						end		
					end
				end
		end
	end
  
  
	// The combinational logic produces the toggle[N:0] signals
	always @(posedge clk) 
		begin     
			check = 0;
			
			toggle[0] =  1'b1;
			toggle[1] = state[0];	
			check[1] = check[1] | state[0];
			for (i=2; i<N; i=i+1) 
				begin	
				// Here goes your code	
					for(j = 0; j<i; j = j+1)
						begin
							check[i] = check[i] | state[j];
						end
					toggle[i] = state[i-1] & !check[i-1];
				end 
			toggle[N] = !(check[i-1]);
			
		
	end
	assign gray_out=state[N:1];
	 
endmodule
